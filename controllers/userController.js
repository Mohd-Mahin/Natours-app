const User = require('../models/userModel');
const AppError = require('../utils/AppError');

exports.getAllUser = async (req, res, next) => {
  try {
    const users = await User.find();
    res.status(200).json({
      status: 'success',
      data: users,
    });
  } catch (error) {
    const err = new AppError(error.message, 404, error);
    next(err);
  }
};

